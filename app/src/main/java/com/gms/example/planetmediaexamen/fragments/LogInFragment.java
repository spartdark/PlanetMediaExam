    public void activateUser(JSONObject gson) {
        //SPARTDARK
        JsonObjectRequest jsonObjReq = new JsonObjectRequest(Request.Method.POST,
                getString(R.string.loginurl), gson,
                new Response.Listener<JSONObject>() {
                    @Override
                    public void onResponse(JSONObject response) {
                        Snackbar.make(rootView, "Bienvenid@", Snackbar.LENGTH_SHORT).show();
                        Log.i("MYResponse",response.toString());
                    }
                }
                , new Response.ErrorListener() {

            @Override
            public void onErrorResponse(VolleyError error) {
                Snackbar.make(rootView, "Ocurrio un error", Snackbar.LENGTH_LONG).show();
            }
        });
        RequestQueue requestQueue = Volley.newRequestQueue(getActivity().getApplicationContext());
        requestQueue.add(jsonObjReq);
    }
